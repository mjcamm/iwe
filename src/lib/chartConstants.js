// Shared visual constants for the canvas-based analysis charts.
// Keeping these centralised so the charts feel consistent across the app.

/** Minimum width in px for a single coloured bar (e.g. one chapter, one paragraph). */
export const MIN_BAR_PX = 28;

/** Maximum width in px for a single bar (so charts don't get too chunky on big screens). */
export const MAX_BAR_PX = 78;

/** Min px per dense data point (sentences in pacing chart, where bars don't apply). */
export const MIN_PX_PER_DENSE_POINT = 4;
